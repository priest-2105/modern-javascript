// Switch Statements

const grade = '';


switch(grade){

    case 'A':
        console.log('You Got an A !');
        break;
    case 'B':
        console.log('You Got an B !');
        break;
    case 'C':
        console.log('You Got a C !');
        break;
    case 'D':
        console.log('You Got a D !');
        break;
    case 'E':
        console.log('You Got an E !');
        break;
    default:
        console.log('Not a valid grade');
            
}

